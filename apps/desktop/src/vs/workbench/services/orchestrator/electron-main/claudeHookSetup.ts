import * as fs from 'fs';
import * as path from 'path';
import { promisify } from 'util';
import { ILogService } from '../../../../platform/log/common/log.js';

const writeFile = promisify(fs.writeFile);
const readFile = promisify(fs.readFile);
const mkdir = promisify(fs.mkdir);
const chmod = promisify(fs.chmod);

const HOOK_SCRIPT_NAME = 'workstreams-notify.sh';

function getClaudeSettingsPath(): string {
	return path.join(process.env['HOME'] || '', '.claude', 'settings.json');
}

function getHookScriptDir(): string {
	return path.join(process.env['HOME'] || '', '.claude', 'hooks');
}

function getHookScriptPath(): string {
	return path.join(getHookScriptDir(), HOOK_SCRIPT_NAME);
}

/**
 * Builds the hook script. Port and token are read from per-terminal
 * environment variables (WORKSTREAMS_HOOK_PORT, WORKSTREAMS_HOOK_TOKEN)
 * injected by the terminal contribution. This makes the script static
 * and independent of the server's dynamic port.
 */
function buildHookScript(): string {
	return `#!/usr/bin/env bash
# Auto-generated by Workstreams — do not edit manually.
# Sends Claude Code lifecycle events to the Workstreams desktop app.
# Claude pipes JSON to stdin with hook_event_name, session_id, cwd, etc.

if [ -z "$WORKSTREAMS_WORKTREE_PATH" ]; then
  exit 0
fi

if [ -z "$WORKSTREAMS_HOOK_PORT" ]; then
  exit 0
fi

# Read hook JSON from stdin to extract the event name
# Use flexible pattern to handle optional whitespace: "key": "value" or "key":"value"
HOOK_JSON=$(cat)
EVENT_TYPE=$(echo "$HOOK_JSON" | grep -oE '"hook_event_name"[[:space:]]*:[[:space:]]*"[^"]*"' | grep -oE '"[^"]*"$' | tr -d '"')

if [ -z "$EVENT_TYPE" ]; then
  exit 0
fi

# When the user presses ESC to interrupt a running tool, Claude Code fires
# PostToolUseFailure with is_interrupt: true. Distinguish this from normal
# tool failures so the server can transition Working → Idle instead of
# keeping the spinner stuck.
if [ "$EVENT_TYPE" = "PostToolUseFailure" ]; then
  IS_INTERRUPT=$(echo "$HOOK_JSON" | grep -oE '"is_interrupt"[[:space:]]*:[[:space:]]*true' || true)
  if [ -n "$IS_INTERRUPT" ]; then
    EVENT_TYPE="PostToolUseInterrupt"
  fi
fi

curl -sG "http://127.0.0.1:$WORKSTREAMS_HOOK_PORT/hook/complete" \\
  --connect-timeout 1 --max-time 2 \\
  --data-urlencode "worktreePath=$WORKSTREAMS_WORKTREE_PATH" \\
  --data-urlencode "eventType=$EVENT_TYPE" \\
  --data-urlencode "token=$WORKSTREAMS_HOOK_TOKEN" \\
  > /dev/null 2>&1 || true
`;
}

/**
 * Installs the notify hook script and registers it in Claude's settings.json.
 * Idempotent — safe to call multiple times.
 *
 * The script reads port/token from environment variables rather than
 * baking them in, so it only needs to be written once regardless of
 * the server's dynamic port.
 */
export async function setupClaudeHooks(logService: ILogService): Promise<void> {
	try {
		// 1. Write the hook script
		const scriptDir = getHookScriptDir();
		await mkdir(scriptDir, { recursive: true });

		const scriptPath = getHookScriptPath();
		await writeFile(scriptPath, buildHookScript());
		await chmod(scriptPath, 0o755);

		logService.info(`[ClaudeHookSetup] Wrote hook script: ${scriptPath}`);

		// 2. Merge hooks into ~/.claude/settings.json
		const settingsPath = getClaudeSettingsPath();
		let settings: Record<string, unknown> = {};
		try {
			const raw = await readFile(settingsPath, 'utf-8');
			settings = JSON.parse(raw);
		} catch {
			// File doesn't exist or is invalid — start fresh
		}

		const hookCommand = scriptPath;
		const hookInner = { type: 'command', command: hookCommand };

		const hooks: Record<string, unknown[]> = (settings['hooks'] as Record<string, unknown[]>) || {};

		// Tool-related hooks use matcher '*' (all tools);
		// non-tool hooks omit matcher entirely. Matches Superset's registration pattern.
		const toolEvents = new Set(['PostToolUse', 'PostToolUseFailure', 'PermissionRequest']);
		const eventsToHook = ['UserPromptSubmit', 'Stop', 'PostToolUse', 'PostToolUseFailure', 'PermissionRequest', 'Notification'];

		let changed = false;
		for (const event of eventsToHook) {
			const existing: unknown[] = hooks[event] || [];
			const wantsMatcher = toolEvents.has(event);

			// Find existing entry index for our hook command
			const existingIdx = existing.findIndex((e: unknown) => {
				if (typeof e !== 'object' || e === null) {
					return false;
				}
				const entry = e as Record<string, unknown>;
				const innerHooks = entry['hooks'];
				if (!Array.isArray(innerHooks)) {
					return false;
				}
				return innerHooks.some((h: unknown) =>
					typeof h === 'object' && h !== null && (h as Record<string, unknown>)['command'] === hookCommand
				);
			});

			if (existingIdx >= 0) {
				// Migrate stale entries to match Superset's pattern
				const entry = existing[existingIdx] as Record<string, unknown>;
				if (wantsMatcher && entry['matcher'] !== '*') {
					entry['matcher'] = '*';
					changed = true;
				} else if (!wantsMatcher && 'matcher' in entry) {
					delete entry['matcher'];
					changed = true;
				}
			} else {
				const entry: Record<string, unknown> = { hooks: [hookInner] };
				if (wantsMatcher) {
					entry['matcher'] = '*';
				}
				existing.push(entry);
				hooks[event] = existing;
				changed = true;
			}
		}

		if (changed) {
			settings['hooks'] = hooks;
			await mkdir(path.dirname(settingsPath), { recursive: true });
			await writeFile(settingsPath, JSON.stringify(settings, null, 2) + '\n');
			logService.info(`[ClaudeHookSetup] Updated Claude settings: ${settingsPath}`);
		} else {
			logService.info(`[ClaudeHookSetup] Hooks already registered`);
		}
	} catch (err) {
		logService.warn(`[ClaudeHookSetup] Failed to set up hooks: ${err}`);
	}
}
